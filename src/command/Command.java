package command;

/**
 * Интерфейс для всех команд, доступных в программе.
 * Каждая команда должна реализовывать этот интерфейс,
 * предоставляя имя, описание и логику выполнения.
 */
public interface Command {
    /**
     * Возвращает имя команды (то, что пользователь вводит в консоли).
     *
     * @return имя команды
     */
    String getName();

    /**
     * Возвращает краткое описание команды для вывода в справке.
     *
     * @return описание команды
     */
    String getDescription();

    /**
     * Выполняет команду с заданными аргументами.
     *
     * @param args аргументы команды (строка после имени команды)
     */
    void execute(String args);
}
